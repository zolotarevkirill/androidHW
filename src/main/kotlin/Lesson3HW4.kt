fun main() {
    println("Программа для вывода перевернутой лесенки из символов #")
    println("--------------------------------------------------")
    
    print("Введите высоту лесенки (положительное число): ")
    val height = readLine()?.toIntOrNull()
    
    if (height == null) {
        println("Ошибка: Введите корректное число")
        return
    }
    
    if (height <= 0) {
        println("Ошибка: Высота должна быть положительным числом")
        return
    }
    
    println("\nВаша перевернутая лесенка:")
    
    // Выводим лесенку
    for (i in height downTo 1) {
        // Выводим i символов # в текущей строке
        repeat(i) {
            print("#")
        }
        // Переходим на новую строку
        println()
    }
    
    // Дополнительная информация
    println("\nИнформация:")
    println("Высота лесенки: $height")
    println("Количество использованных символов #: ${(height * (height + 1)) / 2}")
    
    // Пример использования
    println("\nПример чтения лесенки:")
    println("- Верхняя строка содержит $height символов #")
    println("- Каждая следующая строка содержит на один символ # меньше")
    println("- Последняя строка содержит 1 символ #")
} 